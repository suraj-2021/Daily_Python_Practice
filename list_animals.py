# Three different animals that have a common characteristic. Storing the names of these animals in a list, and then use a for loop to print out the name of each animal.

#creating the list
animals = ['Cat','Lion','Tiger','Cheetah','Panther','Jaguar','Puma',]

#looping through the list and printing each item on the list.
for animal in animals:
    print(animal)

#printing a statement for each animal
for animal in animals:
    print(f"{animal} belongs to felidae family!")
