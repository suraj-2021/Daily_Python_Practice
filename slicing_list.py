#Creating a list and do slicing operations on it.

#creating a list of Names
names = ['Shree','Krishna','Govinda','Hare','Murare','Hey','Natha','Narayana','Vasushadeve','Gopal','Radha','Ramana']

#creating a slice of first three names from the list and printing the slice.
first_three = names[0:3]
print(first_three)

#creating a slice with three names from the middle of the list and printing the slice.
middle_names = names[4:9]
print(middle_names)

#creating a slice with last three name from the list and printing the slice.
last_names = names[9:]
print(last_names)
