#Personalized coffee cups have a default Message. 
#The following function,takes size as argument and Message is a default one.

def make_large_cup(size,message='I Love Python'):
    print(f"\nOur Cup's size is: {size}")
    print(f"\nMessage on our Cup is: {message}")
    
make_large_cup(2)
make_large_cup(3,'Joe rogan wakes up early in the Morning')
make_large_cup(4,'Lex Fridman is brillient Scientist')
make_large_cup(5,'Andrew Huberman is Academically Successful')
make_large_cup(3)
