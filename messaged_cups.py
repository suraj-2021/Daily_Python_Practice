#Let's make our own personalized coffee cups. 
#The following function,takes size and message to be printed on our cup as parameters/argument while calling the function.

def make_cup(size,message):
    print(f"\nOur Cup's size is: {size}")
    print(f"\nMessage on our Cup is: {message}")
    
